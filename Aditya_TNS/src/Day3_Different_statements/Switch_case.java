package Day3_Different_statements;
import java.util.*;
public class Switch_case {

	public static void main(String[] args) {
		char x = 's';
		switch(x) {
		case 'l':
			System.out.println("This code contains letter");
			break;
		case 'i':
			System.out.println("This code contains integer");
			break;
		case 's':
			System.out.println("This code contain Symbols");
			break;
		case 'w':
			System.out.println("This code contains white-spaces");
			break;
			
		}
	}
}



